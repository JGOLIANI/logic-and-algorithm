int number1 = 5;
int number2 = 6;

//bigger
Console.WriteLine(number1 > number2);
//bigger or equal
Console.WriteLine(number1 >= number2);
//smaller
Console.WriteLine(number1 < number2);
//smaller or equal
Console.WriteLine(number1 <= number2);
//equal
Console.WriteLine(number1 == number2);
//not equal
Console.WriteLine(number1 != number2);